#기본자료형 정수,문자열,논리,실수 출력
a = 1004        #10진 정수
b = '임꺽정'     # 문자열
c = True        # 논리값
g = 3.14        # 실수
print('a=',a)
print('b='+b)
print('c=',c)
print('g=',g)

# 0+각 진수명
d = 0b1101011   # 2진수 대입 0binary
e = 0o674       # 8진수 대입 0octal
f = 0x3e5       #16진수 대입 0hex
print('d=',d)
print('e=',e)
print('f=',f)

